	</p>

	<!-- Finding a Market -->
	<h2 class="mb-5 mt-10 text-3xl font-semibold text-gray-700">🔎 Finding a Market</h2>
	<p class="mb-4 text-lg leading-relaxed">
		BigMarket allows users to explore prediction markets using <strong>search and category filters</strong>. You can refine your search based on:
	</p>

	<h3 class="mb-2 mt-6 text-2xl font-semibold text-gray-600">Market Type:</h3>
	<ul class="list-inside list-disc space-y-2 text-lg leading-relaxed">
		<li><strong>Binary</strong> – Two possible outcomes (e.g., <i>Yes/No</i>).</li>
		<li><strong>Categorical</strong> – Multiple possible outcomes (e.g., <i>Winner of an event</i>).</li>
		<li><strong>Scalar</strong> – Numeric-based markets that update daily with a <strong>24-hour cooldown</strong> to ensure fairness.</li>
	</ul>

	<h3 class="mb-2 mt-6 text-2xl font-semibold text-gray-600">Category:</h3>
	<p class="mb-4 text-lg leading-relaxed">Browse markets across <strong>sports, politics, crypto, finance</strong>, and more.</p>

	<p class="mb-6 text-lg leading-relaxed">Once you’ve found an interesting market, click on it to view details.</p>

	<!-- Placing a Prediction -->
	<h2 class="mb-5 mt-10 text-3xl font-semibold text-gray-700">🎯 Placing a Prediction</h2>

	<ol class="list-inside list-decimal space-y-3 text-lg leading-relaxed">
		<li>
			<strong>Ensure your wallet is connected</strong><br />
			Supported wallets: <strong>Asigna, Leather, Xverse</strong>.<br />
			Click <strong>“Connect Wallet”</strong> and authorize the connection.
		</li>

		<li>
			<strong>Choose an outcome</strong><br />
			Each market lists possible outcomes (e.g., <i>Yes/No, Candidate A/Candidate B, Price Above/Below</i>).<br />
			Click on the outcome you want to predict.
		</li>

		<li>
			<strong>Enter your stake amount</strong><br />
			The stake must be in one of the supported tokens:
			<ul class="mt-2 list-inside list-disc pl-5">
				<li><strong>BIG token</strong> (BigMarket’s native token)</li>
				<li><strong>STX</strong> (Stacks blockchain)</li>
				<li><strong>sBTC</strong> (Bitcoin Layer 2, allowing BTC-based staking)</li>
			</ul>
			<p class="mt-2">You <strong>cannot stake more than your wallet balance</strong>.</p>
		</li>

		<li>
			<strong>Confirm the transaction</strong><br />
			Click <strong>"Stake"</strong> and sign the transaction in your wallet.<br />
			Wait for <strong>block confirmation</strong> to finalize your stake.
		</li>
	</ol>

	<!-- Viewing Your Prediction -->
	<h2 class="mb-5 mt-10 text-3xl font-semibold text-gray-700">📊 Viewing Your Prediction</h2>
	<p class="mb-4 text-lg leading-relaxed">After staking, you can track your position:</p>

	<h3 class="mb-2 mt-6 text-2xl font-semibold text-gray-600">Market Page</h3>
	<ul class="list-inside list-disc space-y-2 text-lg leading-relaxed">
		<li>Displays <strong>your stake and chosen outcome</strong>.</li>
		<li>Shows <strong>total value locked (TVL)</strong> in each potential outcome.</li>
	</ul>

	<h3 class="mb-2 mt-6 text-2xl font-semibold text-gray-600">Graphical Insights</h3>
	<ul class="list-inside list-disc space-y-2 text-lg leading-relaxed">
		<li>Market trends and prediction data are <strong>visualized</strong> to help with decision-making.</li>
		<li>See how the odds shift as other users stake.</li>
	</ul>

	<!-- Important Rules -->
	<h2 class="mb-5 mt-10 text-3xl font-semibold text-gray-700">⚠️ Important Rules</h2>
	<ul class="list-inside list-disc space-y-2 text-lg leading-relaxed">
		<li>✅ A <strong>connected wallet</strong> is required before staking.</li>
		<li>✅ You <strong>cannot stake more</strong> than your available balance.</li>
		<li>✅ <strong>Scalar markets</strong> reset daily with a <strong>24-hour cooldown</strong>.</li>
		<li>✅ Once staked, <strong>your prediction is locked</strong> until market resolution.</li>
	</ul>

